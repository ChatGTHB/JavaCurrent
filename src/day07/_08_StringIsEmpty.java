package day07;

public class _08_StringIsEmpty {

    public static void main(String[] args) {

        // IsEmpty : Is there one or more characters in a string or not?
        // boolean

        String s1 = "Hello";
        String s2 = "";

        System.out.println("s1.isEmpty() = " + s1.isEmpty()); // s1 isEmpty  false
        System.out.println("s2.isEmpty() = " + s2.isEmpty()); // true
    }
}
