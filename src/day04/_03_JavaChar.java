package day04;

public class _03_JavaChar {

    public static void main(String[] args) {

        // char is actually the number in the background

        char firstLetter = 'A';  // A -> 65, characters are enclosed in single quotes.
        // because " " it means String, and ' ' ' means character

        System.out.println("firstLetter = " + firstLetter);

        char nameFirstLetter = 'J';     // Joseph's J
        char surnameFirstLetter = 'B';  // Burns's B

        System.out.println(nameFirstLetter + "." + surnameFirstLetter + "."); // J.B.
    }
}